    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          try {
        String tanggalStr = txtTanggal.getText().trim();
        double persen1 = Double.parseDouble(txtDiskon1.getText());
        double persen2 = Double.parseDouble(txtDiskon2.getText());

        if (tanggalStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tanggal tidak boleh kosong!");
            return;
        }

        // ðŸ”¸ ubah String ke java.sql.Date (format: yyyy-MM-dd)
        java.sql.Date tanggal = java.sql.Date.valueOf(tanggalStr);

        Diskon d = new Diskon(tanggal, persen1, persen2);
        new DiskonDao().insertDiskon(d);

        JOptionPane.showMessageDialog(this, "Diskon berhasil ditambahkan!");
        loadDiskon();

    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Format tanggal harus yyyy-MM-dd!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menambah diskon: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = tabelDiskon.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data diskon yang ingin diubah!");
        return;
    }

    // Ambil data dari tabel
    int id = (int) tabelDiskon.getValueAt(row, 0);
    String tglLama = tabelDiskon.getValueAt(row, 1).toString();
    double persen1Lama = Double.parseDouble(tabelDiskon.getValueAt(row, 2).toString());
    double persen2Lama = Double.parseDouble(tabelDiskon.getValueAt(row, 3).toString());

    // Input baru dari user
    String tglBaruStr = JOptionPane.showInputDialog(this, "Tanggal (yyyy-MM-dd):", tglLama);
    if (tglBaruStr == null || tglBaruStr.trim().isEmpty()) return;

    String persen1Str = JOptionPane.showInputDialog(this, "Diskon 1 (%):", persen1Lama);
    if (persen1Str == null || persen1Str.trim().isEmpty()) return;

    String persen2Str = JOptionPane.showInputDialog(this, "Diskon 2 (%):", persen2Lama);
    if (persen2Str == null || persen2Str.trim().isEmpty()) return;

    try {
        java.sql.Date tanggal = java.sql.Date.valueOf(tglBaruStr.trim());
        double persen1 = Double.parseDouble(persen1Str);
        double persen2 = Double.parseDouble(persen2Str);

        Diskon d = new Diskon(tanggal, persen1, persen2);
        d.setId(id);

        DiskonDao dao = new DiskonDao();
        dao.updateDiskon(d);

        JOptionPane.showMessageDialog(this, "Diskon berhasil diperbarui!");
        loadDiskon(); // refresh tabel
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Format tanggal harus yyyy-MM-dd!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal ubah diskon: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tabelDiskon.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data diskon yang ingin dihapus!");
        return;
    }

    int id = (int) tabelDiskon.getValueAt(row, 0);
    int konfirmasi = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus data diskon ini?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            DiskonDao dao = new DiskonDao();
            dao.deleteDiskon(id);
            JOptionPane.showMessageDialog(this, "Diskon berhasil dihapus!");
            loadDiskon(); // refresh tabel
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal hapus diskon: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void setupTabelDiskon() {
    // atur model tabel di luar initComponents
    DefaultTableModel model = new DefaultTableModel(
        new Object[]{"ID", "Tanggal", "Diskon 1", "Diskon 2"}, 0
    );
    tabelDiskon.setModel(model);
    }
    
    private void loadDiskon() {
    DiskonDao dao = new DiskonDao();
    List<Diskon> list = dao.getSemuaDiskon();

    DefaultTableModel model = (DefaultTableModel) tabelDiskon.getModel();
    model.setRowCount(0); // hapus data lama

    for (Diskon d : list) {
        model.addRow(new Object[]{
            d.getId(),
            d.getTanggal(),
            d.getPersen1(),
            d.getPersen2()
        });
    }
    }


        
   

