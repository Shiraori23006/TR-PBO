    private void hapusProduk() {
    int row = tabelProduk.getSelectedRow();
    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih produk yang ingin dihapus!");
        return;
    }

    int id = (int) tabelProduk.getValueAt(row, 0);
    int confirm = JOptionPane.showConfirmDialog(this, 
                "Yakin ingin menghapus produk ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if(confirm != JOptionPane.YES_OPTION) return;

    ProductDao dao = new ProductDao();
    dao.deleteProduk(id);

    loadProduk(); // refresh tabel
    JOptionPane.showMessageDialog(this, "Produk berhasil dihapus!");
}
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        // Ambil input user
        String nama = txtProduk.getText();
        Double harga = Double.parseDouble(txtHargaProduk.getText());

        if (nama.isEmpty() || harga <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Pastikan nama, harga (>0) dan stok (>=0) valid!");
            return;
        }

        // Buat objek Produk
        Produk p = new Produk(nama, harga);

        // Insert ke database
        ProductDao dao = new ProductDao();
        dao.insertProduk(p);

        // Tampilkan pesan sukses
        javax.swing.JOptionPane.showMessageDialog(this, "Produk berhasil ditambahkan!");

        // Bersihkan field input
        txtProduk.setText("");
        txtHargaProduk.setText("");

        // Refresh tabel
        loadProduk();

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Harga dan Stok harus angka!");
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Gagal menambahkan produk: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

